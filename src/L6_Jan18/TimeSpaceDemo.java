public class TimeSpaceDemo {

	public static void main(String[] args) {

		int n = 10000;

		for (int i = 1; i <= n; i++) {
			System.out.println(i);
		}

		for (int i = 1; i <= n; i++) {
			System.out.println(i);
		}

		System.out.println("hi");
		System.out.println("hi");

		for (int i = 1; i <= n; i++) {

			for (int j = 1; j <= n; j=j*2) {

				System.out.println("hello");
				
			}
		}

	}
}
















